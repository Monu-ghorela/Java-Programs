package loops.assgn;

public class Q10 {
    public static void main(String[] args) {
       for (int i = 1; i <=7; i++) {
        System.out.println("*");
        
       } 
       System.out.println();
       int i=1;
       while (i<=7) {
        i++;
        System.out.println("*");
        
       }
       System.out.println();
       int j=1;
       do{
        System.out.println("*");
        j++;
       }while(j<=7);
    }
}
