package loops.assgn;

public class Q2 {

  public static void main(String[] args) {
    for (int i = 1; i < 10; i++) {
      System.out.println(i);
    }
    System.out.println();
    int i=1;
    while (i<10) {
        System.out.println(i++);
        
    }
    System.out.println();
    int j=1;
    do{
        System.out.println(j++);

    }while(j<10);
    
  }
}
