package Methods.assgn3;

public class Q12 {

  public static void square(int n) {
    System.out.println("the square of the number " + n + " is " + (n * n));
  }

  public static void main(String[] args) {
    int n = 10;
    square(n);
    square(12);
    square(13);
    square(4);
    square(7);
    square(9);
  }
}
